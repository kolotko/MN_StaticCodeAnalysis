namespace MN_StaticCodeAnalysis.LogicForTestCoverage;

public static class MathematicalOperations
{
    public static int Add(int a, int b)
    {
        return a + b;
    }
    
    public static decimal Subtraction(int a, int b)
    {
        return a - b;
    }
    
    public static decimal Multiplication(int a, int b)
    {
        return a * b;
    }
    
    public static decimal Division(int a, int b)
    {
        return a / b;
    }
}